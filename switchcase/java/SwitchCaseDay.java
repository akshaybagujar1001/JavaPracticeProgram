package com.switchcase.java;

public class SwitchCaseDay {
    public static void main(String[] args) {
        int day;
        day = 3;
        switch (day){
            case 1 :
                System.out.println("mon");
                break;
            case 2 :
                System.out.println("Tue");
                break;
            case 3:
                System.out.println("Wed");
                break;
        }
    }
}
